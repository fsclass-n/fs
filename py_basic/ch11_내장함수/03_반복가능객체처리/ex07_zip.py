# zip
# zip(*iterable)은 동일한 개수로 이루어진 데이터들을 묶어서 반환


# 대응하는 것끼리 튜플로 묶어서 출력
print(zip([1,2,3,4], [4,5,6]))
# <zip object at 0x0000026B1965E100>
print(list(zip([1,2,3,4], [4,5,6]))) 
# [(1, 4), (2, 5), (3, 6)]

print(list(zip([1,2,3],[4,5,6],[7,8,9])))
# [(1, 4, 7), (2, 5, 8), (3, 6, 9)]

print(list(zip("abc", "def")))
# [('a', 'd'), ('b', 'e'), ('c', 'f')]